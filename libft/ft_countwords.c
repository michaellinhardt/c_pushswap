#include "libft.h"

int		ft_countwords(const char *s, char c)
{
	size_t	i;
	int		count;
	int		start;
	int		end;

	i = 0;
	count = 0;
	while (s[i] != '\0')
	{
		while (s[i] && s[i] == c)
			i++;
		start = i;
		while (s[i] && s[i] != c)
			i++;
		end = i;
		if (end > start)
			count++;
	}
	return (count);
}
